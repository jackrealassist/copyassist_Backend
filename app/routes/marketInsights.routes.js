const controller = require("../controllers/marketInsights.controller");

module.exports = function(app) {
  app.use(function(req, res, next) {
    res.header(
      "Access-Control-Allow-Headers",
      "x-access-token, Origin, Content-Type, Accept"
    );
    next();
  });

  app.get("/api/market-insights/measures/:regionType/zipcode", controller.marketInsights);
};
